/*
  Descomplicando os JOINS
    A ideia do JOIN é permitir combinar registros de duas ou mais tabelas, através
    do relacionamento que uma tabela tem com a outra.
    
  O que é um JOIN?
    Em alguns casos, uma tabela pode não possuir toda a informação necessária.
    Em função disso, existe a opção de usar os diversos tipos de JOIN para combinar
    em um mesmo resultado registros de duas ou mais tabelas.

    Esses tipos são: INNER JOIN, LEFT JOIN e RIGHT JOIN, para combinar duas ou mais
    tabelas, e SELF JOIN, quando uma tabela precisa ser combinada consigo mesma.
    Você verá detalhes de cada um desses tipos de JOIN a seguir.
*/